#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n >= '0' && n <= '9')
	{
		write(fd, &n, 1);
	}
}
int main()
{
	ft_putnbr_fd('2', 1);
	return (0);
}
