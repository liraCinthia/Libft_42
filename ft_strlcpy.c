#include "libft.h"

size_t	ft_strlcpy(char *dst, const char *src, size_t size)
{
	size_t		src_leng;
	size_t		i;

	src_leng = ft_strlen(src);
	i = 0;
	if (size == 0)
	{
		return (src_leng);
	}
	while (src[i] != '\0' && i < size - 1)
	{
		dst[i] = src[i];
		i++;
	}
	dst[i] = '\0';
	return (src_leng);
}
